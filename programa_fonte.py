def calcularSoma(valor1, valor2):
    valor3 = valor1 + valor2
    return valor3

def calcularSubtracao(valor1, valor2):
    valor3 = valor1 - valor2
    return valor3

def calcularMultiplicacao(valor1, valor2):
    valor3 = valor1 * valor2
    return valor3

resultado = calcularSoma(1, 2)
x = 0

while (x <= resultado):
    x = x + 1
if x > resultado:
    print(resultado)

